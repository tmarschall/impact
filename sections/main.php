      <h1>How to Use this Calculator</h1>
      <p>We designed this Impact Calculator to help answer the first question on a donor's mind: where does my money go?  While it's easy to focus on overhead percentages, the real impact of a charity is reflected in what that charity can do with its budget.  A donor who cares about reducing extreme poverty has hundreds of organizations to choose from – some of which are thousands of times more effective than others.  Our Impact Calculator highlights the work of twelve of the world's most effective charities, allowing you to compare each organization – and the evidence supporting each intervention – side by side.</p>
      <p>Determining a charity's cost-effectiveness can be a complicated endeavor, and we could never hope to reflect our recommended charities' full impact in one simple online tool.  Instead, we've tried to create a starting point you can use in order to kickstart your investigation into impact-oriented philanthropy.  We encourage you to use this as one resource among many in your charity assessments.  <a href="http://www.thelifeyoucansave.org/About-Us/Contact-Us">Contact Us</a> if you need further support – and don't forget to bring your friends and family in on the conversation!</p>
