      <section id="organisation">
        <h1>The Organization</h1>
        <p>GiveDirectly provides unconditional cash transfers via cell phone to some of the poorest families in Kenya. Households are selected using criteria such as proximity to public services and quality of housing. GiveDirectly then transfers $1,000 over one to two years to the chosen households using a Kenyan mobile payment system, and the recipients are free to use the money as they wish.</p>
      </section>

      <section id="numbers">
        <h1>The Numbers</h1>
        <p>Cash transfers are relatively well studied and the available research is very promising. Several studies have found that cash transfers improve child health and schooling, while savings and investment rates are typically high. GiveDirectly is committed to transparently tracking and improving their effectiveness through randomized controlled trials (the “gold standard” in social sciences research). Targeting the extreme poor (recipients live on $0.65 a day on average) allows GiveDirectly to dramatically affect recipients’ lives with each cash transfer. GiveDirectly’s overhead is 10% (keep in mind that the overhead ratio is not a direct measurement of charity effectiveness).</p>
      </section>

